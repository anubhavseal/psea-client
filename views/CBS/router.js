angular.module('cbs')
.config(['$routeProvider',function($routeProvider){
    $routeProvider
    .when('/cbs-home',{
        templateUrl:'/views/CBS/profile/view',
        controller:'cbs.home.controller'
    })
    .when('/cbs-home/profile-listing',{
        templateUrl:'/views/CBS/profile/i_ProfileListingView',
        controller:'cbs.home.controller'
    });
}]);